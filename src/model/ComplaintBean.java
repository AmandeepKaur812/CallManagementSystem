package model;

public class ComplaintBean 
{
	private String complaintID;
	private String customerTelephone;
	private String productSerialNo;
	private String productCategory;
	private String operatorUname;
	private String complaintDate;
	private String complainDetail;
	private String complaintSolution;
	private String rootCause;
	private String endDate;
	
	
	
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	
	public String getComplaintSolution() {
		return complaintSolution;
	}
	public void setComplaintSolution(String complaintSolution) {
		this.complaintSolution = complaintSolution;
	}
	public String getRootCause() {
		return rootCause;
	}
	public void setRootCause(String rootCause) {
		this.rootCause = rootCause;
	}
	public String getComplaintID() {
		return complaintID;
	}
	public void setComplaintID(String complaintID) {
		this.complaintID = complaintID;
	}
	public String getCustomerTelephone() {
		return customerTelephone;
	}
	public void setCustomerTelephone(String customerTelephone) {
		this.customerTelephone = customerTelephone;
	}
	public String getProductSerialNo() {
		return productSerialNo;
	}
	public void setProductSerialNo(String productSerialNo) {
		this.productSerialNo = productSerialNo;
	}
	public String getProductCategory() {
		return productCategory;
	}
	public void setProductCategory(String productCategory) {
		this.productCategory = productCategory;
	}
	public String getOperatorUname() {
		return operatorUname;
	}
	public void setOperatorUname(String operatorUname) {
		this.operatorUname = operatorUname;
	}
	
	public String getComplaintDate() {
		return complaintDate;
	}
	public void setComplaintDate(String complaintDate) {
		this.complaintDate = complaintDate;
	}
	public String getComplainDetail() {
		return complainDetail;
	}
	public void setComplainDetail(String complainDetail) {
		this.complainDetail = complainDetail;
	}
	
	
}
